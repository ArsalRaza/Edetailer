public class PresentationBean {

	private String mName = "";
	private String mPathOnSdCard = "";
	private String mImageUrl = "";
	private String mIndexFilePath = "";
	private String mSampleFilePath = "";
	public String getName() {
		return mName;
	}
	public void setName(String mName) {
		this.mName = mName;
	}
	public String getPathOnSdCard() {
		return mPathOnSdCard;
	}
	public void setPathOnSdCard(String mPathOnSdCard) {
		this.mPathOnSdCard = mPathOnSdCard;
	}
	public String getImageUrl() {
		return mImageUrl;
	}
	public void setImageUrl(String mImageUrl) {
		this.mImageUrl = mImageUrl;
	}
	public String getmIndexFilePath() {
		return mIndexFilePath;
	}
	public void setmIndexFilePath(String mIndexFilePath) {
		this.mIndexFilePath = mIndexFilePath;
	}
	public String getmSampleFilePath() {
		return mSampleFilePath;
	}
	public void setmSampleFilePath(String mSampleFilePath) {
		this.mSampleFilePath = mSampleFilePath;
	}
	
	
}
